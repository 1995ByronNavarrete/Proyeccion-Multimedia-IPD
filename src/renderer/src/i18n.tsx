const DICT: Record<string, Record<string, string>> = { es: ES, en: EN }

interface LangCtx {
  lang: string
  setLang: (l: string) => void
  t: (key: string) => string
}

const Ctx = createContext<LangCtx>({ lang: 'es', setLang: () => {}, t: (k) => k })

export function LangProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState(() => {
    try { return localStorage.getItem(STORAGE_KEY) || 'es' } catch { return 'es' }
  })

  const setLang = useCallback((l: string) => {
    setLangState(l)
    try { localStorage.setItem(STORAGE_KEY, l) } catch {}
  }, [])

  const t = useCallback((key: string): string => {
    return DICT[lang]?.[key] || DICT['es']?.[key] || key
  }, [lang])

  return <Ctx.Provider value={{ lang, setLang, t }}>{children}</Ctx.Provider>
}

export function useLang() {
  return useContext(Ctx)
}
